public class WhileSimplesShrek {
    public static void main(String[] args) {
        // While (Enquanto)
        // While (<Condição>){}
        int contador = 0;
        while (contador < 7 ) {
            System.out.println("Já chegou");
            contador ++; // Mesma coisa: contador = contador +1; 
        }
        System.out.println("Ainda não chegou, Cala a boca burro !!!");
    }
}
